package org.usfirst.frc.frc2992.MyRobot.lib;

public class Coordinate2D {
	
	double x;
	double y;
	
	public Coordinate2D(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	public void setX_Coor(double x){
		this.x = x;
	}
	
	public void setY_Coor(double y){
		this.y = y;
	}
}
